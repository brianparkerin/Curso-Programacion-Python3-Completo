# Set or Conjunto in python

conjunto = {1,2,3}

print(conjunto)
print(type(conjunto))

# Los datos de Set o Conjunto no pueden ser repetidos
# Pues al momento de print los que esten repetidos los va a eliminar y nos va amostrar solo los que esten una vez

Conjunto_2 = {1,2,3,1,2,3}

print(Conjunto_2)
print(type(Conjunto_2))

# Los sets nos permite tbn "Validar Pertenencia"
# Podriamos preguntar o validar si el 2 esta dentro de un conjunto
print(conjunto)
print(2 in conjunto)

# Nos retorna un "True" ya que el 2 si hace parte del "Set" de datos de nuestra especie "de lista", o de este tipo de estructura de datos.

print(conjunto)
print(4 in conjunto)

# Nos retorna un "False" ya que el 4 no hace parte del "Set" de datos de nuestra especie "de lista"









